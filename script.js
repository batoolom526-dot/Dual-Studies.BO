document.addEventListener('DOMContentLoaded', function() {
    setupEventListeners();
    renderPartners();
    renderExperiences();
    drawWorldMap();
    setLanguage('en');
});

// Setup event listeners
function setupEventListeners() {
    // Language toggle
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', function() {
            const lang = this.dataset.lang;
            setLanguage(lang);
        });
    });

    // Buttons
    document.getElementById('shareBtn').addEventListener('click', openModal);
    document.getElementById('shareBtn2').addEventListener('click', openModal);
    document.getElementById('exploreBtn').addEventListener('click', (e) => {
        // If the control is an anchor (has an href), allow navigation to partners.html.
        // Only intercept clicks to scroll when the control is NOT a link.
        const isAnchor = e.currentTarget.tagName.toLowerCase() === 'a' || e.currentTarget.getAttribute('href');
        const partnersSection = document.querySelector('.partners-section');

        if (partnersSection && !isAnchor) {
            // For legacy non-anchor buttons on pages that still include the partners section,
            // prevent default and scroll into view.
            e.preventDefault();
            partnersSection.scrollIntoView({ behavior: 'smooth' });
        } else {
            // If this is an anchor (like the hero Explore link), do nothing and allow the browser to navigate.
            // If it's not an anchor and partners section is absent, navigate programmatically.
            if (!isAnchor && !partnersSection) {
                window.location.href = 'partners.html';
            }
        }
    });
    document.getElementById('joinBtn').addEventListener('click', () => {
        window.location.href = 'programs.html';
    });
    document.getElementById('submitBtn').addEventListener('click', openModal);

    // Modal
    document.getElementById('closeModal').addEventListener('click', closeModal);
    document.getElementById('cancelBtn').addEventListener('click', closeModal);
    document.getElementById('experienceForm').addEventListener('submit', handleFormSubmit);

    // Click outside modal to close
    document.getElementById('experienceModal').addEventListener('click', function(e) {
        if (e.target === this) {
            closeModal();
        }
    });

    // Filters
    document.getElementById('programFilter').addEventListener('change', filterPartners);
    document.getElementById('typeFilter').addEventListener('change', filterPartners);
    document.getElementById('regionFilter').addEventListener('change', filterPartners);

    // Demo toggle
    document.getElementById('demoToggle').addEventListener('change', function() {
        console.log('Demo mode:', this.checked);
    });
}

// Language management
function setLanguage(lang) {
    currentLanguage = lang;
    document.documentElement.lang = lang;
    
    if (lang === 'ar') {
        document.body.dir = 'rtl';
    } else {
        document.body.dir = 'ltr';
    }

    // Update all translatable content
    Object.keys(translations[lang]).forEach(key => {
        const element = document.getElementById(key);
        if (element) {
            element.textContent = translations[lang][key];
        }
    });

    // Update language button active state
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.dataset.lang === lang) {
            btn.classList.add('active');
        }
    });

    // Re-render content for language change
    renderPartners();
    renderExperiences();
}

// Render partners
function renderPartners() {
    const grid = document.getElementById('partnersGrid');
    grid.innerHTML = '';

    partnersData.forEach(partner => {
        const card = document.createElement('div');
        card.className = 'partner-card';
        card.innerHTML = `
            <div class="partner-name">${partner.name}</div>
            <div class="partner-type">${partner.type}</div>
            <div class="partner-location">${partner.location}</div>
            <div class="partner-tags">
                ${partner.tags.map(tag => `<span class="tag">${tag}</span>`).join('')}
            </div>
            <div class="partner-since">${partner.since}</div>
        `;
        grid.appendChild(card);
    });
}

// Render experiences
function renderExperiences() {
    const grid = document.getElementById('experiencesGrid');
    grid.innerHTML = '';

    experiencesData.forEach(exp => {
        const card = document.createElement('div');
        card.className = 'experience-card';
        
        const name = currentLanguage === 'ar' ? exp.nameAr : exp.name;
        const story = currentLanguage === 'ar' ? exp.storyAr : exp.story;
        const storyClass = currentLanguage === 'ar' ? 'experience-story experience-story-ar' : 'experience-story';
        
        card.innerHTML = `
            <div class="experience-name">${name}</div>
            <div class="experience-program">${exp.program}</div>
            <div class="experience-company">${exp.company}</div>
            <div class="experience-badge">${exp.status}</div>
            <div class="${storyClass}">${story}</div>
        `;
        grid.appendChild(card);
    });
}

// Filter partners
function filterPartners() {
    const programFilter = document.getElementById('programFilter').value;
    const typeFilter = document.getElementById('typeFilter').value;
    const regionFilter = document.getElementById('regionFilter').value;

    const grid = document.getElementById('partnersGrid');
    const cards = grid.querySelectorAll('.partner-card');

    cards.forEach(card => {
        let show = true;

        if (programFilter && !card.textContent.includes(programFilter)) {
            show = false;
        }

        if (typeFilter && !card.textContent.includes(typeFilter)) {
            show = false;
        }

        if (regionFilter && !card.textContent.includes(regionFilter)) {
            show = false;
        }

        card.style.display = show ? 'block' : 'none';
    });
}

// Modal management
function openModal() {
    document.getElementById('experienceModal').classList.add('active');
}

function closeModal() {
    document.getElementById('experienceModal').classList.remove('active');
    document.getElementById('experienceForm').reset();
}

